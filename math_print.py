import math


def golden_ratio():
    print((1 + 5 ** (1 / 2)) / 2)


def six_squared():
    print(6 ** 2)


def hypotenuse():
    print(math.hypot(5, 12))


def pi():
    print(math.pi)


def e():
    print(math.e)


def squares_area():
    print(11 * 1, 11 * 2, 11 * 3, 11 * 4, 11 * 5, 11 * 6, 11 * 7, 11 * 8, 11 * 9, 11 * 10)


if __name__ == "__main__":
    golden_ratio()
    six_squared()
    hypotenuse()
    pi()
    e()
    squares_area()
